    "–ò–Ω—Ç–µ—Ä–µ—Å–Ω—ã –ª–∏ –≤–∞–º –º–µ–∂–¥–∏—Å—Ü–∏–ø–ª–∏–Ω–∞—Ä–Ω—ã–µ –ø—Ä–æ–≥—Ä–∞–º–º—ã?",
    "–•–æ—Ç–µ–ª–∏ –±—ã –≤—ã —É—á–∞—Å—Ç–≤–æ–≤–∞—Ç—å –≤ —Å—Ç—É–¥–µ–Ω—á–µ—Å–∫–∏—Ö –æ–±–º–µ–Ω–∞—Ö?",
    "–í–∞–∂–Ω–æ –ª–∏ –¥–ª—è –≤–∞—Å –Ω–∞–ª–∏—á–∏–µ –æ–±—â–µ–∂–∏—Ç–∏—è?",
    "–ö–∞–∫–æ–≤—ã –≤–∞—à–∏ –æ–∂–∏–¥–∞–Ω–∏—è –æ—Ç –≤—É–∑–∞?",
