def get_random_questions(num=5):
    """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å–ø–∏—Å–æ–∫ —Å–ª—É—á–∞–π–Ω—ã—Ö –≤–æ–ø—Ä–æ—Å–æ–≤"""
    return random.sample(QUESTIONS, num)

def get_chatgpt_response(prompt):
    """–û—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –∑–∞–ø—Ä–æ—Å –∫ ChatGPT –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –æ—Ç–≤–µ—Ç"""
    response = openai.ChatCompletion.create(
        model="gpt-3.5-turbo",  # –∏–ª–∏ "gpt-4" –µ—Å–ª–∏ –¥–æ—Å—Ç—É–ø–Ω–æ
        messages=[
            {"role": "system", "content": "–¢—ã –∫–æ–Ω—Å—É–ª—å—Ç–∞–Ω—Ç –ø–æ –ø—Ä–æ—Ñ–æ—Ä–∏–µ–Ω—Ç–∞—Ü–∏–∏. –ù–∞ –æ—Å–Ω–æ–≤–µ –æ—Ç–≤–µ—Ç–æ–≤ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —Å–æ—Å—Ç–∞–≤—å —Ä–µ–π—Ç–∏–Ω–≥ –∏–∑ 5 –ø–æ–¥—Ö–æ–¥—è—â–∏—Ö –≤—É–∑–æ–≤ —Å –æ–±—ä—è—Å–Ω–µ–Ω–∏–µ–º, –ø–æ—á–µ–º—É –∫–∞–∂–¥—ã–π –≤—É–∑ –ø–æ–¥—Ö–æ–¥–∏—Ç. –û–ø–∏—à–∏ –∫–æ–Ω–∫—Ä–µ—Ç–Ω—ã–µ –ø—Ä–æ–≥—Ä–∞–º–º—ã –∏ –ø—Ä–µ–∏–º—É—â–µ—Å—Ç–≤–∞."},
            {"role": "user", "content": prompt}
        ],
        temperature=0.7
    )
    return response.choices[0].message['content']

def main():
    st.title("üéì –ü—Ä–æ—Ñ–æ—Ä–∏–µ–Ω—Ç–∞—Ü–∏—è: –ø–æ–¥–±–æ—Ä –≤—É–∑–∞")
    st.write("–û—Ç–≤–µ—Ç—å—Ç–µ –Ω–∞ 5 —Å–ª—É—á–∞–π–Ω—ã—Ö –≤–æ–ø—Ä–æ—Å–æ–≤, –∏ –º—ã –ø–æ–¥–±–µ—Ä–µ–º –ø–æ–¥—Ö–æ–¥—è—â–∏–µ –≤—É–∑—ã –Ω–∞ –æ—Å–Ω–æ–≤–µ –≤–∞—à–∏—Ö –æ—Ç–≤–µ—Ç–æ–≤")

    # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è —Å–µ—Å—Å–∏–∏
    if 'answers' not in st.session_state:
        st.session_state.answers = {}
        st.session_state.questions = get_random_questions(5)
        st.session_state.stage = "questions"

    # –≠—Ç–∞–ø –≤–æ–ø—Ä–æ—Å–æ–≤
    if st.session_state.stage == "questions":
        with st.form("questions_form"):
            for i, question in enumerate(st.session_state.questions):
                st.session_state.answers[question] = st.text_input(f"{i + 1}. {question}", key=f"q_{i}")

            submitted = st.form_submit_button("–û—Ç–ø—Ä–∞–≤–∏—Ç—å –æ—Ç–≤–µ—Ç—ã")
            if submitted:
                if all(st.session_state.answers.values()):
                    st.session_state.stage = "results"
                    st.rerun()
                else:
                    st.warning("–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –æ—Ç–≤–µ—Ç—å—Ç–µ –Ω–∞ –≤—Å–µ –≤–æ–ø—Ä–æ—Å—ã")

    # –≠—Ç–∞–ø —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
    elif st.session_state.stage == "results":
        st.success("–°–ø–∞—Å–∏–±–æ –∑–∞ –æ—Ç–≤–µ—Ç—ã! –ê–Ω–∞–ª–∏–∑–∏—Ä—É–µ–º –≤–∞—à–∏ –ø—Ä–µ–¥–ø–æ—á—Ç–µ–Ω–∏—è...")

        # –§–æ—Ä–º–∏—Ä—É–µ–º –ø—Ä–æ–º–ø—Ç –¥–ª—è ChatGPT
        prompt = "–û—Ç–≤–µ—Ç—ã –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –Ω–∞ –≤–æ–ø—Ä–æ—Å—ã –ø—Ä–æ—Ñ–æ—Ä–∏–µ–Ω—Ç–∞—Ü–∏–∏:\n"
        for question, answer in st.session_state.answers.items():
            prompt += f"- –í–æ–ø—Ä–æ—Å: {question}\n  –û—Ç–≤–µ—Ç: {answer}\n\n"

        prompt += "\n–ü—Ä–æ–∞–Ω–∞–ª–∏–∑–∏—Ä—É–π –æ—Ç–≤–µ—Ç—ã –∏ –ø—Ä–µ–¥–ª–æ–∂–∏ 5 –Ω–∞–∏–±–æ–ª–µ–µ –ø–æ–¥—Ö–æ–¥—è—â–∏—Ö –≤—É–∑–æ–≤ –≤ –†–æ—Å—Å–∏–∏ —Å –æ–±—ä—è—Å–Ω–µ–Ω–∏–µ–º, –ø–æ—á–µ–º—É –∫–∞–∂–¥—ã–π –≤—É–∑ –ø–æ–¥—Ö–æ–¥–∏—Ç. –£–∫–∞–∂–∏ –∫–æ–Ω–∫—Ä–µ—Ç–Ω—ã–µ –ø—Ä–æ–≥—Ä–∞–º–º—ã –∏ –∏—Ö –ø—Ä–µ–∏–º—É—â–µ—Å—Ç–≤–∞ –¥–ª—è —ç—Ç–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è."

        # –ü–æ–ª—É—á–∞–µ–º –æ—Ç–≤–µ—Ç –æ—Ç ChatGPT
        with st.spinner("–ü–æ–¥–±–∏—Ä–∞–µ–º –≤—É–∑—ã..."):
            try:
                response = get_chatgpt_response(prompt)
                st.session_state.recommendations = response
            except Exception as e:
                st.error(f"–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞: {e}")
                st.session_state.stage = "questions"
                return
    # –í—ã–≤–æ–¥–∏–º —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏
        st.subheader("–†–µ–∫–æ–º–µ–Ω–¥—É–µ–º—ã–µ –≤—É–∑—ã –¥–ª—è –≤–∞—Å:")
        st.write(st.session_state.recommendations)

        if st.button("–ü–æ–ø—Ä–æ–±–æ–≤–∞—Ç—å —Å–Ω–æ–≤–∞"):
            st.session_state.answers = {}
            st.session_state.questions = get_random_questions(5)
            st.session_state.stage = "questions"
            st.rerun()

if __name__ == "__main__":
    main()